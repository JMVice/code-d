# Variable sin valor.

n = None

# Variable de tipo número.

x = 5
y = 10

# Variables de tipo cadena de texto o String.

nombre = "Alberto"
otro_nombre = 'Carlos'

# Función print()

print(x) # => 5
print(y) # => 10
print() # => 'Salto de linea'.
print(nombre) # => Alberto
print(otro_nombre) # => Carlos
print()
# Print con salto de linea incluido en string.
print('Hola\nMundo') # => Hola
                    # => Mundo

# Output:

'''
5
10

Alberto
Carlos

Hola
Mundo
'''
